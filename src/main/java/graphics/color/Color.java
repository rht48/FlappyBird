package graphics.color;

public class Color {
    private final int red;
    private final int green;
    private final int blue;

    public Color(final int r, final int g, final int b) {
        red = r;
        green = g;
        blue = b;
    }

    public int getRed() {
        return red;
    }

    public int getGreen() {
        return green;
    }

    public int getBlue() {
        return blue;
    }


}

